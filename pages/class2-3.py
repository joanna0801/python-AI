for i in range(10):  # i是迴圈的變數, range(10)是從0到9的數字
    print(i)  # 只從0到9的數字顯示

for i in range(2, 100):  # i是迴圈的變數, range(10)是從0到9的數字
    print(i)  # 從2到99的數字顯示

for i in range(2, 100, 2):  # i是迴圈的變數, range(10)是從0到9的數字
    print(i)  # 從2到99的數字顯示

for i in range(100, 3, -2):  # i是迴圈的變數, range(10)是從0到9的數字
    print(i)  # 從100到4的數字顯示
